# Integer
numero_int = 21
print("Inteiro = ", numero_int)

# num real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante= ", numero_real)

# type() - retorna o tipo da variavel no output
print("tipo da variavel inteiro = ", type(numero_int))
print("Tipo da variavel real = ", type(numero_real))

# soma = +
soma = 67 + 58
print("Soma = ", soma)


# subtração = -
subtracao = 67 - 58
print("Subtração = ", subtracao)

# multiplicação = *
multiplicacao = 67 * 58
print("Multiplicação = ", multiplicacao)

# divisão = /
divisao = 67 / 58
print("Divisão = ", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))

# função de conversão int()
print("Valor em inteiro = ", int(divisao))

# funcão de conversão float()
print("Valor em real com ponto flutuante = ", float(soma))


# Modulo = % retorna o resto da divisão
modulo = 5 % 2
print("Modulo = ", modulo)

# divisão inteiro = //
divisao = 67 // 58
print("Divisão = ", divisao)
print("Tipo da variavel do resultado da divisão = ", type(divisao))